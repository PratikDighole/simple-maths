def area_square(n):
    """
    Takes the length of a square and returns the
    area of the square, given by n*n
    """
    area = n*n
    return area

def perimeter_square(n):
    """
    Take the length of the side of a square
    and calculate the perimeter, given by 4*n.
    """
    perimeter = 4*n
    return perimeter
